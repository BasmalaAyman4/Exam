let prevButton = document.getElementById("prev-button");
let nextButton = document.getElementById("next-button")
let paginations = document.querySelector('.paginations');
let currentPage = 1;


exams.forEach(data => {

    paginations.innerHTML += `<button class="paginationBtn" btn-index="${data.id}" onclick="activeBtn(${data.id})">${data.id}</button>`
    if (data.id == 1) {
        document.querySelector('.paginationBtn').classList.add("activeBtn")
    }
})


function buttonsStatus() {

    if (currentPage === 1) {
        prevButton.setAttribute("disabled", "");
    } else {
        prevButton.removeAttribute("disabled", "");
    }
    if (currentPage === exams.length) {
        nextButton.setAttribute("disabled", "");
    } else {
        nextButton.removeAttribute("disabled", "");
    }
};
function prev() {
    activeBtn(currentPage - 1);
}
function next() {
    activeBtn(currentPage + 1);
}
function activeBtn(btnNum) {
    currentPage = btnNum;
    buttonsStatus();
    document.querySelectorAll('.paginationBtn').forEach(btn => {

        btn.classList.remove("activeBtn")
        let pageIndex = Number(btn.getAttribute("btn-index"))

        if (pageIndex == btnNum) {
            btn.classList.add("activeBtn")
        }
    })
    let queAndAns = document.querySelectorAll('.queAndAns')
    queAndAns.forEach(ele => {
        ele.classList.add('d-none')
    })
    queAndAns[btnNum - 1].classList.remove('d-none')
}


